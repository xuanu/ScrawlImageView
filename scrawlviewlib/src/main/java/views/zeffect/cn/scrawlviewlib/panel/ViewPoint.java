public class ViewPoint {
    /**
     * X坐标
     */
    private float x;
    /**
     * Y坐标
     */
    private float y;

    public float getX() {
        return x;
    }

    public void setX(float pX) {
        x = pX;
    }

    public float getY() {
        return y;
    }

    public void setY(float pY) {
        y = pY;
    }
}
